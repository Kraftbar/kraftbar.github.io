
/* ------ Toggle hiding of element ------ */
/* -------------------------------------- */

function myFunction(id_elm) {
  var x = document.getElementById(id_elm);
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
